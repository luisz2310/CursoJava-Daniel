/**
 * Ejemplo básico de variables en Java.
 */
public class DeclararEImprimo_Ejemplo {

    // Todo programa en Java inicia en el método main
    public static void main(String[] args) {

        // =========================
        // VARIABLES EN JAVA
        // =========================

        // Declarar una variable (sin valor inicial)
        // Sintaxis: tipo + nombre;
        byte m;
        // Asignar un valor a la variable
        m = 2;
        System.out.println(m);
        
        // Declarar e inicializar en una sola línea
        // Sintaxis: tipo + nombre = valor;
        byte n1 = 4;
        System.out.println(n1);
        
        char letra = 'a';
        System.out.println(letra);

    }
}